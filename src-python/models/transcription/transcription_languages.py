def _applyEngineLanguageCodes() -> None:
    for language, countries in transcription_lang.items():
        vosk_code = VOSK_SUPPORTED_LANGUAGE_CODES.get(language)
        parakeet_code = PARAKEET_SUPPORTED_LANGUAGE_CODES.get(language)
        for country, engines in countries.items():
            if vosk_code:
                engines["Vosk"] = vosk_code
            if parakeet_code:
                engines["Parakeet"] = parakeet_code

    # Vosk's Chinese models are Mandarin, not Cantonese.
    if "Chinese Traditional" in transcription_lang:
        transcription_lang["Chinese Traditional"].get("Hong Kong", {}).update({"Vosk": ""})


_applyEngineLanguageCodes()
